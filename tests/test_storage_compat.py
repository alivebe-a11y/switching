# Round-trip: load → save → reload yields identical counts + key fields
# ---------------------------------------------------------------------------

def test_load_save_reload_us_no_data_loss(cache_dir):
    """The single biggest regression risk: save() drops rows on write back."""
    from switching.paper_trader import Portfolio
    before = Portfolio.load(_us_path(cache_dir))
    before.save(_us_path(cache_dir))                # no mutation — pure round-trip
    storage._reset_connection_cache()
    after = Portfolio.load(_us_path(cache_dir))

    assert len(after.positions) == len(before.positions)
    assert len(after.trades) == len(before.trades)
    assert after.cash == pytest.approx(before.cash)
    assert sorted(after.seen_signals) == sorted(before.seen_signals)
    assert after.recently_sold == before.recently_sold
    assert after.pending_orders == before.pending_orders
    # Severity field — if a future refactor drops it, this fails loud
    for b, a in zip(before.trades, after.trades):
        assert a.ticker == b.ticker
        assert a.exit_reason == b.exit_reason
        assert a.severity == b.severity


