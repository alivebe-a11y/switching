def test_services_remain_isolated_after_save(cache_dir):
    """Save on one service must NOT touch another service's rows."""
    from switching.paper_trader import Portfolio
    uk_before = Portfolio.load(_uk_path(cache_dir))
    uk_n_pos, uk_n_trades = len(uk_before.positions), len(uk_before.trades)
    # Mutate + save US
    us = Portfolio.load(_us_path(cache_dir))
    us.cash += 100.0
    us.save(_us_path(cache_dir))
    storage._reset_connection_cache()
    # UK rows unchanged
    uk_after = Portfolio.load(_uk_path(cache_dir))
    assert len(uk_after.positions) == uk_n_pos
    assert len(uk_after.trades) == uk_n_trades
    assert uk_after.cash == pytest.approx(uk_before.cash)
