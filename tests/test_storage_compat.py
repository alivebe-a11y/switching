"""Backwards-compat tests against a frozen v1 SQLite fixture.

The fixture (``tests/fixtures/switching_v1.db``) was generated by
``scripts/build_test_fixture.py`` and represents the storage shape we
write today (Portfolio scalars + blobs, positions, closed_trades,
exit_tracks, skipped_signals, trade_memory, dropped_signals).

Every test in this file copies the fixture to a fresh tmp_path then loads
it via the **public** APIs (``Portfolio.load`` etc).  If a future code
change drops a column, renames a key, or otherwise breaks the load path,
these tests fail in CI before the change can merge — protecting the
trade history we've already collected.

When a schema change is **intentional**, regenerate the fixture
(``python scripts/build_test_fixture.py``) and update the assertions in
this file in the SAME commit, with a message explaining what changed.
"""

from __future__ import annotations

import shutil
from pathlib import Path

import pytest

from switching import storage


FIXTURE_PATH = Path(__file__).parent / "fixtures" / "switching_v1.db"


@pytest.fixture
def cache_dir(tmp_path):
    """Copy the frozen v1 db into a tmp cache dir and yield the paths."""
    assert FIXTURE_PATH.exists(), (
        f"fixture missing: {FIXTURE_PATH}.  Run "
        "`python scripts/build_test_fixture.py` to regenerate."
    )
    shutil.copy(FIXTURE_PATH, tmp_path / "switching.db")
    storage._reset_connection_cache()
    yield tmp_path
    storage._reset_connection_cache()


def _us_path(cache_dir):  return cache_dir / "paper_portfolio.json"
def _uk_path(cache_dir):  return cache_dir / "uk_portfolio.json"
def _t212_path(cache_dir): return cache_dir / "t212_portfolio.json"


# ---------------------------------------------------------------------------
# Portfolio (3 services, each via Portfolio.load)
# ---------------------------------------------------------------------------

def test_load_v1_us_portfolio_intact(cache_dir):
    from switching.paper_trader import Portfolio
    p = Portfolio.load(_us_path(cache_dir))
    # Cash + scalars
    assert p.cash == pytest.approx(18_432.55)
    assert p.max_position_pct == pytest.approx(0.015)
    assert p.max_positions == 50
    # Positions
    assert len(p.positions) == 2
    tickers = {pos.ticker for pos in p.positions}
    assert tickers == {"ACME", "RNDX"}
    acme = next(x for x in p.positions if x.ticker == "ACME")
    assert acme.detector == "guidance_raise"
    assert acme.entry_price == pytest.approx(40.0)
    assert acme.first_green is True
    assert len(acme.snapshots) == 1
    rndx = next(x for x in p.positions if x.ticker == "RNDX")
    assert rndx.peak_tracking is True
    # Closed trades — locks in the 3 different exit_reasons
    assert len(p.trades) == 3
    reasons = {t.exit_reason for t in p.trades}
    assert reasons == {"first_green", "stop_loss", "corporate_action"}
    # Severity column survived (added recently)
    assert all(t.severity is not None for t in p.trades)
    # Blobs
    assert len(p.seen_signals) == 3
    assert "OLD" in p.recently_sold
    # Cached prices restored
    assert p.cached_prices.get("ACME") == pytest.approx(42.1)


